    /**
     * Po wpisaniu liczby do jTextField1 i wciśnięciu entera ukazuje nam się 
     * tableka jTable1 do wprowadzania danych
     * 
     * @param evt 
     */
    private void jTextField1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jTextField1ActionPerformed
        try
        {
            n = Integer.parseInt(jTextField1.getText());
        }
        catch(NumberFormatException e) 
        { 
            showMessageDialog(null,"Wpisałeś inny znak niż liczba! Popraw się");
        }
        if(n<0 && n==(int)n)
        {
            showMessageDialog(null, "n nie może być ujemne! Podaj nowe n.");
        }
        else
        {
            String m[] = {"Nr równania", "Współczynniki"};
            model = new DefaultTableModel(null,m);
            jTable1.setModel(model);
            jTable1.putClientProperty("terminateEditOnFocusLost", Boolean.TRUE);
            Macierz = new CObliczenia(n);
            Macierz.PokazDane(jTable1, model);
        }
    }//GEN-LAST:event_jTextField1ActionPerformed

