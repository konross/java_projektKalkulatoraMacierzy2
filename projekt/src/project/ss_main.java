    /**
     * Po wciśnięciu przycisku 'Wczytaj' wczytane zostaną dane z ówcześniej
     * wybranego pliku i wyświetlone w jTable1.
     * @param evt 
     */
    private void wczytajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_wczytajActionPerformed
        String m[] = {"Nr równania", "Współczynniki"};
        model = new DefaultTableModel(null,m);
        jTable1.setModel(model);
        jTable1.putClientProperty("terminateEditOnFocusLost", Boolean.TRUE);
        try {
            plik.Wczytaj(jTable1, model, jTextField2);
        } catch (FileNotFoundException ex) {
            Logger.getLogger(ss_main.class.getName()).log(Level.SEVERE, null, ex);
            showMessageDialog(null, "Brak takiego pliku!");
        }
        n=plik.ilosc();
        Macierz = new CObliczenia(n);
    }//GEN-LAST:event_wczytajActionPerformed

