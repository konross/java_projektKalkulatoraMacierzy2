    /**
     * Po wciśnięciu przycisku 'Oblicz' dokonywane są wszystkie obliczenia na
     * podstawie danych z jTable1, a wyniki są wyświetlane w jTable2.
     * 
     * @param evt 
     */
    private void ObliczActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ObliczActionPerformed
        if(n>0)
        {
            String m[] = {"Nr X'a", "Wartość"};
            model = new DefaultTableModel(null,m);
            jTable2.setModel(model);
            jTable2.putClientProperty("terminateEditOnFocusLost", Boolean.TRUE);
            Macierz.PrzypiszDane(jTable1);
            if(Macierz.WyznaczWartosci()==2)
            {
                Macierz.PrzypiszRozw(jTable2, model);
            }
            else if(Macierz.WyznaczWartosci()==1)
            {
                showMessageDialog(null, "Dzielenie przez zero! Brak rozwiązań!");
            }
        }
        else
        {
            showMessageDialog(null, "Brak danych do wykonania obliczeń!");
        }
    }//GEN-LAST:event_ObliczActionPerformed

